# Pedir al usuario el resumen
resumen = str(input("Ingrese un resumen breve: "))
resumen = resumen[:50]
print(resumen)

# Longitud total del resumen
longitud = len(resumen)
print(f"La longitud del resuemn es: {longitud}")

# Convertir el resumen a mayusculas y minusculas
mayusculas = resumen.upper()
minusculas = resumen.lower()

print(f"El resumen en mayusculas: {mayusculas}")
print(f"El resumen en minusculas: {minusculas}")

# Buscar la cantidad de "e"
cantidad_e = resumen.lower().count('e')
print(f"La cantidad de 'e' que hay en el resumen son: {cantidad_e}")

# Resumen con sus palabras unidas por comas
print(resumen.split())
