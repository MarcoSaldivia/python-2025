#ALGUNAS FUNCIONES CON DATOS NUMÉRICOS
# float( ) Convierte un valor a un número decimal

# int ( ) Convierte un valor a un entero

# min ( ) Devuelve el valor más pequeño de uno o más valores

# max ( ) Devuelve el valor más grande de uno o más valores

# round ( ) Redondea un número a un número específico de dígitos decimales

# sum ( ) Devuelve la suma de una lista de números



#ALGUNAS FUNCIONES CON STRINGS
# len( ) Devuelve la longitud de la cadena 

# split ( ) Divide la cadena en subcadenas utilizando un separador especificado

# join ( ) Une strings utilizando la cadena de texto que lo llama como separador.

# str ( ) Convierte un objeto en un string

# upper ( ) Devuelve una copia del string en mayúsculas

# lower ( ) Devuelve una copia del string en minúsculas



# ALGUNAS FUNCIONES CON LISTAS
# append ( ) Agrega un elemento al final de la lista

# insert ( ) Inserta un elemento en una posición específica de la lista

# extend ( ) Básicamente, esta función combina dos o más listas

# remove ( ) Elimina la primera ocurrencia de un elemento en la lista

# pop ( ) Borra y retorna el último elemento de una lista

# sort ( ) Ordena los elementos de la lista de manera ascendente



# ALGUNAS FUNCIONES CON TUPLAS
# count ( ) Devuelve el número de veces que aparece el valor en la tupla.

# index ( ) Devuelve el índice del elemento seleccionado en la tupla

# sorted ( ) Entrega una lista ordenada con los elementos de la tupla



# ALGUNAS FUNCIONES CON SETS
# add ( ) Agrega un elemento al conjunto

# clear ( ) Elimina todos los elementos del set

# discard ( ) Elimina un elemento del set. Si el elemento no existe, no hace nada.

# intersection ( ) Devuelve un nuevo set que contiene sólo los elementos que están en ambos sets.

# difference ( ) Al contrario de intersection() devuelve un nuevo set que contiene sólo
#  los elementos que están en el 1° set pero no en el 2° set

# copy ( ) Crea una nueva instancia del set que contiene las mismas referencias a los
# elementos del set original



# ALGUNAS FUNCIONES CON DICCIONARIOS
# keys ( ) Devuelve una lista con todas las claves del diccionario

# values ( ) Devuelve una lista con todos los valores del diccionario

# items ( ) Devuelve una lista de tuplas, donde cada tupla contiene una clave y su respectivo valor.



# OTRAS FUNCIONES
# map ( ) Es una función que sirve para procesar el contenido de objetos iterables. Es decir,
# puede servir de reemplazo de un bucle for en Python.

# filter ( ) Se encarga de filtrar elementos de un iterable, aplicando una función que 
# devuelve True o False. Funciona con: listas, sets, tuplas y diccionarios.

# zip ( ) Esta función toma dos o más iterables (como listas, tuplas, etc) y los combina en un solo
# iterable de tuplas, donde cada tupla contiene elementos en las posiciones correspondientes de
# los iterables de entrada.
